
# basic datatypes in python

a = 'hello world'
b = 12
c = 12.5
d = True

# python built in type() function is used to check the datatypes of variables

print(type(a))
print(type(b))
print(type(c))
print(type(d))

e = [1,'hello',12.5,['yes','no']]
print(type(e))

f = (1,'helo',True)

print(type(f))

g = {'name':'riley','age':18,12:'number 12'}
print(type(g))